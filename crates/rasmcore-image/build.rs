//! Build script — scans for #[register_filter] annotations and generates:
//! 1. WIT filter declarations (printed to stderr for manual review/copy)
//! 2. Rust adapter dispatch code (written to $OUT_DIR/generated_filter_adapter.rs)
//!
//! The generated adapter code is included in adapter/mod.rs via:
//!   include!(concat!(env!("OUT_DIR"), "/generated_filter_adapter.rs"));

use std::env;
use std::fs;
use std::path::{Path, PathBuf};

use serde_json::{Value, json};

fn main() {
    let manifest_dir = env::var("CARGO_MANIFEST_DIR").unwrap();
    let filters_path = Path::new(&manifest_dir).join("src/domain/filters.rs");
    let param_types_path = Path::new(&manifest_dir).join("src/domain/param_types.rs");
    let composite_path = Path::new(&manifest_dir).join("src/domain/composite.rs");

    // Tell cargo to rerun if filters.rs, param_types.rs, composite.rs, or build.rs changes
    println!("cargo:rerun-if-changed=src/domain/filters.rs");
    println!("cargo:rerun-if-changed=src/domain/param_types.rs");
    println!("cargo:rerun-if-changed=src/domain/composite.rs");
    println!("cargo:rerun-if-changed=build.rs");

    if !filters_path.exists() {
        return;
    }

    // Parse each source file separately via syn (concatenation breaks AST parsing)
    let filters_source = fs::read_to_string(&filters_path).unwrap();
    let param_types_source = if param_types_path.exists() {
        fs::read_to_string(&param_types_path).unwrap()
    } else {
        String::new()
    };
    let composite_source = if composite_path.exists() {
        fs::read_to_string(&composite_path).unwrap()
    } else {
        String::new()
    };

    // Collect filters from all source files
    let mut filters = parse_registered_filters(&filters_source);
    if !composite_source.is_empty() {
        filters.extend(parse_registered_filters(&composite_source));
    }

    // Each file parsed separately via syn — no combined source needed

    if filters.is_empty() {
        // Write empty generated file
        let out_dir = PathBuf::from(env::var("OUT_DIR").unwrap());
        fs::write(
            out_dir.join("generated_filter_adapter.rs"),
            "// No registered filters found\n",
        )
        .unwrap();
        return;
    }

    // Duplicate detection — fail at compile time if two filters share a name
    {
        let mut seen: std::collections::HashMap<&str, &str> = std::collections::HashMap::new();
        for f in &filters {
            if let Some(existing_fn) = seen.get(f.name.as_str()) {
                panic!(
                    "\n\nDuplicate filter name '{}' registered by:\n  {} (fn {})\n  {} (fn {})\n\
                     Rename one to resolve the conflict.\n",
                    f.name, f.fn_name, f.fn_name, existing_fn, existing_fn
                );
            }
            seen.insert(&f.name, &f.fn_name);
        }
    }

    // Print summary to stderr (visible during build)
    eprintln!(
        "rasmcore build.rs: Found {} registered filter(s):",
        filters.len()
    );
    for f in &filters {
        let params: Vec<String> = f.params.iter().map(|(n, t)| format!("{n}: {t}")).collect();
        eprintln!(
            "  {} ({}): {}({})",
            f.name,
            f.category,
            f.fn_name,
            params.join(", ")
        );
    }

    // Generate WIT declarations (printed to stderr for review)
    eprintln!("\n--- Generated WIT (copy to filters.wit if new filters added) ---");
    for f in &filters {
        let wit_params = f
            .params
            .iter()
            .map(|(n, t)| format!("{}: {}", to_wit_name(n), to_wit_type(t)))
            .collect::<Vec<_>>()
            .join(", ");

        let wit_sig = if wit_params.is_empty() {
            format!(
                "    {}: func(pixels: buffer, info: image-info) -> result<buffer, rasmcore-error>;",
                to_wit_name(&f.name)
            )
        } else {
            format!(
                "    {}: func(pixels: buffer, info: image-info, {}) -> result<buffer, rasmcore-error>;",
                to_wit_name(&f.name),
                wit_params
            )
        };
        eprintln!("{wit_sig}");
    }
    eprintln!("--- End WIT ---\n");

    // Generate Rust adapter dispatch code
    let out_dir = PathBuf::from(env::var("OUT_DIR").unwrap());
    let mut adapter_code = String::new();
    adapter_code.push_str("// Auto-generated by build.rs from #[register_filter] annotations.\n");
    adapter_code.push_str("// Do not edit — regenerate by changing filters.rs and rebuilding.\n\n");
    adapter_code.push_str("impl filters::Guest for Component {\n");

    for f in &filters {
        let trait_method = &f.name;
        let domain_fn = &f.fn_name;

        // Map Rust domain types to WIT binding types for the trait signature,
        // and generate the call expression (adding & for slice params)
        let mut sig_params = Vec::new();
        let mut call_params = Vec::new();
        for (n, t) in &f.params {
            let clean_n = n.trim_start_matches('_');
            let (binding_type, call_expr) = match t.as_str() {
                "&[f32]" => ("Vec<f32>", format!("&{clean_n}")),
                "&[f64]" => ("Vec<f64>", format!("&{clean_n}")),
                "&[u8]" => ("Vec<u8>", format!("&{clean_n}")),
                "&[u32]" => ("Vec<u32>", format!("&{clean_n}")),
                "&str" => ("String", format!("&{clean_n}")),
                "String" => ("String", clean_n.to_string()),
                _ => (t.as_str(), clean_n.to_string()), // scalars pass through
            };
            sig_params.push(format!("{clean_n}: {binding_type}"));
            call_params.push(call_expr);
        }

        let full_sig = if sig_params.is_empty() {
            "pixels: Vec<u8>, info: types::ImageInfo".to_string()
        } else {
            format!(
                "pixels: Vec<u8>, info: types::ImageInfo, {}",
                sig_params.join(", ")
            )
        };

        let full_call = if call_params.is_empty() {
            "&pixels, &di".to_string()
        } else {
            format!("&pixels, &di, {}", call_params.join(", "))
        };

        adapter_code.push_str(&format!(
            "    fn {trait_method}({full_sig}) -> Result<Vec<u8>, RasmcoreError> {{\n"
        ));
        adapter_code.push_str("        let di = to_domain_image_info(&info);\n");
        adapter_code.push_str(&format!(
            "        domain::filters::{domain_fn}({full_call}).map_err(to_wit_error)\n"
        ));
        adapter_code.push_str("    }\n\n");
    }

    adapter_code.push_str("}\n"); // close impl filters::Guest for Component

    fs::write(out_dir.join("generated_filter_adapter.rs"), &adapter_code).unwrap();

    eprintln!(
        "rasmcore build.rs: Generated filter adapter ({} filters)",
        filters.len()
    );

    // ── Generate pipeline filter nodes ────────────────────────────────────
    //
    // Each registered filter gets a typed node struct + ImageNode impl.
    // The node stores the upstream node ID, source image info, and all params.
    // compute_region fetches upstream pixels and calls the domain filter function.

    let mut nodes_code = String::new();
    nodes_code.push_str("// Auto-generated pipeline filter nodes.\n");
    nodes_code.push_str("// Do not edit — regenerate by changing filters.rs and rebuilding.\n\n");
    nodes_code.push_str("use crate::domain::error::ImageError;\n");
    nodes_code.push_str("use crate::domain::filters;\n");
    nodes_code.push_str("use crate::domain::pipeline::graph::{AccessPattern, ImageNode, bytes_per_pixel, crop_region};\n");
    nodes_code.push_str("use crate::domain::types::*;\n");
    nodes_code.push_str("use rasmcore_pipeline::{Overlap, Rect};\n\n");

    for f in &filters {
        let node_name = format!("{}Node", to_pascal_case(&f.name));
        let domain_fn = &f.fn_name;

        // Struct fields: upstream, source_info, and each param
        nodes_code.push_str(&format!("pub struct {node_name} {{\n"));
        nodes_code.push_str("    upstream: u32,\n");
        nodes_code.push_str("    source_info: ImageInfo,\n");
        for (pname, ptype) in &f.params {
            let clean_n = pname.trim_start_matches('_');
            let owned_type = to_owned_type(ptype);
            nodes_code.push_str(&format!("    {clean_n}: {owned_type},\n"));
        }
        nodes_code.push_str("}\n\n");

        // Constructor
        let ctor_params: Vec<String> = f
            .params
            .iter()
            .map(|(n, t)| {
                let clean_n = n.trim_start_matches('_');
                format!("{clean_n}: {}", to_owned_type(t))
            })
            .collect();
        let ctor_sig = if ctor_params.is_empty() {
            "upstream: u32, source_info: ImageInfo".to_string()
        } else {
            format!(
                "upstream: u32, source_info: ImageInfo, {}",
                ctor_params.join(", ")
            )
        };
        let ctor_fields: Vec<String> = f
            .params
            .iter()
            .map(|(n, _)| n.trim_start_matches('_').to_string())
            .collect();
        let mut all_fields = vec!["upstream".to_string(), "source_info".to_string()];
        all_fields.extend(ctor_fields);

        nodes_code.push_str(&format!("impl {node_name} {{\n"));
        nodes_code.push_str(&format!("    pub fn new({ctor_sig}) -> Self {{\n"));
        nodes_code.push_str(&format!("        Self {{ {} }}\n", all_fields.join(", ")));
        nodes_code.push_str("    }\n");
        nodes_code.push_str("}\n\n");

        // ImageNode impl
        let overlap_expr = match f.overlap.as_str() {
            "full" => "Overlap::uniform(u32::MAX)".to_string(),
            s if s.starts_with("uniform(") => format!("Overlap::{s}"),
            s if s.starts_with("param(") => {
                let param_name = s.trim_start_matches("param(").trim_end_matches(')');
                format!("Overlap::uniform(self.{param_name} as u32)")
            }
            _ => "Overlap::zero()".to_string(), // default: point operation
        };

        // Build the domain function call
        let call_args: Vec<String> = f
            .params
            .iter()
            .map(|(n, t)| {
                let clean_n = n.trim_start_matches('_');
                if t.starts_with("&[") || t == "&str" {
                    format!("&self.{clean_n}")
                } else if t == "String" {
                    format!("self.{clean_n}.clone()")
                } else {
                    format!("self.{clean_n}")
                }
            })
            .collect();
        let full_domain_call = if call_args.is_empty() {
            format!("filters::{domain_fn}(&src_pixels, &region_info)")
        } else {
            format!(
                "filters::{domain_fn}(&src_pixels, &region_info, {})",
                call_args.join(", ")
            )
        };

        nodes_code.push_str(&format!("impl ImageNode for {node_name} {{\n"));
        nodes_code.push_str("    fn info(&self) -> ImageInfo { self.source_info.clone() }\n\n");
        nodes_code.push_str("    fn compute_region(\n");
        nodes_code.push_str("        &self,\n");
        nodes_code.push_str("        request: Rect,\n");
        nodes_code.push_str(
            "        upstream_fn: &mut dyn FnMut(u32, Rect) -> Result<Vec<u8>, ImageError>,\n",
        );
        nodes_code.push_str("    ) -> Result<Vec<u8>, ImageError> {\n");
        nodes_code.push_str("        let overlap = self.overlap();\n");
        nodes_code.push_str("        let upstream_rect = request.expand(&overlap, self.source_info.width, self.source_info.height);\n");
        nodes_code
            .push_str("        let src_pixels = upstream_fn(self.upstream, upstream_rect)?;\n");
        nodes_code.push_str("        let region_info = ImageInfo {\n");
        nodes_code.push_str("            width: upstream_rect.width,\n");
        nodes_code.push_str("            height: upstream_rect.height,\n");
        nodes_code.push_str("            ..self.source_info\n");
        nodes_code.push_str("        };\n");
        nodes_code.push_str(&format!("        let filtered = {full_domain_call}?;\n"));
        nodes_code.push_str("        if upstream_rect == request {\n");
        nodes_code.push_str("            Ok(filtered)\n");
        nodes_code.push_str("        } else {\n");
        nodes_code.push_str("            let bpp = bytes_per_pixel(self.source_info.format);\n");
        nodes_code.push_str("            let sub = Rect::new(\n");
        nodes_code.push_str("                request.x - upstream_rect.x,\n");
        nodes_code.push_str("                request.y - upstream_rect.y,\n");
        nodes_code.push_str("                request.width,\n");
        nodes_code.push_str("                request.height,\n");
        nodes_code.push_str("            );\n");
        nodes_code.push_str("            let out_rect = Rect::new(0, 0, upstream_rect.width, upstream_rect.height);\n");
        nodes_code.push_str("            Ok(crop_region(&filtered, out_rect, sub, bpp))\n");
        nodes_code.push_str("        }\n");
        nodes_code.push_str("    }\n\n");
        nodes_code.push_str(&format!(
            "    fn overlap(&self) -> Overlap {{ {overlap_expr} }}\n"
        ));
        nodes_code.push_str(
            "    fn access_pattern(&self) -> AccessPattern { AccessPattern::LocalNeighborhood }\n",
        );
        nodes_code.push_str("}\n\n");
    }

    fs::write(out_dir.join("generated_pipeline_nodes.rs"), &nodes_code).unwrap();

    // ── Generate pipeline adapter filter methods ──────────────────────────
    //
    // Each filter gets a method on PipelineResource that creates the typed node
    // and adds it to the graph.

    let mut pipe_adapter = String::new();
    pipe_adapter.push_str("// Auto-generated pipeline filter adapter methods.\n");
    pipe_adapter.push_str("// Do not edit — regenerate by changing filters.rs and rebuilding.\n\n");
    pipe_adapter.push_str("macro_rules! generated_pipeline_filter_methods {\n");
    pipe_adapter.push_str("    () => {\n");

    for f in &filters {
        let trait_method = &f.name;
        let node_name = format!("{}Node", to_pascal_case(&f.name));

        // WIT binding signature params
        let mut sig_params = Vec::new();
        let mut node_ctor_args = Vec::new();
        for (n, t) in &f.params {
            let clean_n = n.trim_start_matches('_');
            let binding_type = to_binding_type(t);
            sig_params.push(format!("{clean_n}: {binding_type}"));
            // For slice types, the node stores owned data; pass directly from binding
            node_ctor_args.push(clean_n.to_string());
        }

        let full_sig = if sig_params.is_empty() {
            "&self, source: NodeId".to_string()
        } else {
            format!("&self, source: NodeId, {}", sig_params.join(", "))
        };

        let ctor_call = if node_ctor_args.is_empty() {
            format!("filters::{node_name}::new(source, src_info)")
        } else {
            format!(
                "filters::{node_name}::new(source, src_info, {})",
                node_ctor_args.join(", ")
            )
        };

        pipe_adapter.push_str(&format!(
            "    fn {trait_method}({full_sig}) -> Result<NodeId, RasmcoreError> {{\n"
        ));
        pipe_adapter.push_str("        let src_info = self.graph.borrow().node_info(source).map_err(to_wit_error)?;\n");
        pipe_adapter.push_str(&format!("        let node = {ctor_call};\n"));
        pipe_adapter.push_str("        Ok(self.graph.borrow_mut().add_node(Box::new(node)))\n");
        pipe_adapter.push_str("    }\n\n");
    }

    pipe_adapter.push_str("    };\n"); // close macro arm
    pipe_adapter.push_str("}\n"); // close macro_rules!

    fs::write(out_dir.join("generated_pipeline_adapter.rs"), &pipe_adapter).unwrap();

    eprintln!(
        "rasmcore build.rs: Generated pipeline nodes + adapter ({} filters)",
        filters.len()
    );

    // ── Generate param-manifest.json via serde_json ────────────────────────
    // Parse ConfigParams structs from each file separately, then merge
    let mut param_structs = parse_config_params_structs(&param_types_source);
    param_structs.extend(parse_config_params_structs(&filters_source));
    if !composite_source.is_empty() {
        param_structs.extend(parse_config_params_structs(&composite_source));
    }
    let mut filter_entries: Vec<Value> = Vec::new();

    for f in &filters {
        let struct_name = format!("{}Params", to_pascal_case(&f.name));
        let params_struct = param_structs.get(&struct_name);

        let params_json: Vec<Value> = if let Some(fields) = params_struct {
            fields
                .iter()
                .map(|field| {
                    json!({
                        "name": field.name,
                        "type": field.param_type,
                        "min": parse_json_value(&field.min),
                        "max": parse_json_value(&field.max),
                        "step": parse_json_value(&field.step),
                        "default": parse_json_value(&field.default_val),
                        "label": field.label,
                        "hint": field.hint,
                    })
                })
                .collect()
        } else if !f.params.is_empty() {
            f.params
                .iter()
                .map(|(pname, ptype)| {
                    let (min, max, step, def) = default_range_for_type(ptype);
                    json!({
                        "name": pname,
                        "type": ptype,
                        "min": parse_json_value(&min),
                        "max": parse_json_value(&max),
                        "step": parse_json_value(&step),
                        "default": parse_json_value(&def),
                        "label": "",
                        "hint": "",
                    })
                })
                .collect()
        } else {
            vec![]
        };

        filter_entries.push(json!({
            "name": f.name,
            "category": f.category,
            "group": f.group,
            "variant": f.variant,
            "reference": f.reference,
            "params": params_json,
        }));
    }

    // Parse generators, compositors, mappers from each file separately
    let mut generators = parse_registered_by_kind(&filters_source, "register_generator");
    let mut compositors = parse_registered_by_kind(&filters_source, "register_compositor");
    let mut mappers = parse_registered_by_kind(&filters_source, "register_mapper");
    if !composite_source.is_empty() {
        generators.extend(parse_registered_by_kind(
            &composite_source,
            "register_generator",
        ));
        compositors.extend(parse_registered_by_kind(
            &composite_source,
            "register_compositor",
        ));
        mappers.extend(parse_registered_by_kind(
            &composite_source,
            "register_mapper",
        ));
    }

    let gen_entries: Vec<serde_json::Value> = generators
        .iter()
        .map(|g| {
            json!({
                "name": g.name,
                "category": g.category,
                "group": g.group,
                "variant": g.variant,
                "reference": g.reference,
                "kind": "generator",
            })
        })
        .collect();

    let comp_entries: Vec<serde_json::Value> = compositors
        .iter()
        .map(|c| {
            json!({
                "name": c.name,
                "category": c.category,
                "group": c.group,
                "variant": c.variant,
                "reference": c.reference,
                "kind": "compositor",
            })
        })
        .collect();

    let map_entries: Vec<serde_json::Value> = mappers
        .iter()
        .map(|m| {
            json!({
                "name": m.name,
                "category": m.category,
                "group": m.group,
                "variant": m.variant,
                "reference": m.reference,
                "kind": "mapper",
            })
        })
        .collect();

    let manifest = json!({
        "filters": filter_entries,
        "generators": gen_entries,
        "compositors": comp_entries,
        "mappers": map_entries,
    });
    let manifest_str = serde_json::to_string_pretty(&manifest).unwrap();
    fs::write(out_dir.join("param-manifest.json"), &manifest_str).unwrap();
    eprintln!(
        "rasmcore build.rs: Generated param-manifest.json ({} filters, {} generators, {} compositors, {} mappers)",
        filters.len(),
        generators.len(),
        compositors.len(),
        mappers.len()
    );
}

/// Parsed filter registration.
struct FilterReg {
    name: String,
    category: String,
    group: String,
    variant: String,
    reference: String,
    overlap: String, // "zero" (default), "uniform(N)", "full", "param(name)"
    fn_name: String,
    params: Vec<(String, String)>, // (param_name, rust_type)
}

/// Parse #[register_filter(...)] annotations from Rust source via syn AST.
fn parse_registered_filters(source: &str) -> Vec<FilterReg> {
    let file = match syn::parse_file(source) {
        Ok(f) => f,
        Err(e) => {
            eprintln!("build.rs: syn parse error: {e}");
            return Vec::new();
        }
    };

    let mut filters = Vec::new();
    for item in &file.items {
        if let syn::Item::Fn(func) = item {
            if let Some(reg) = extract_filter_reg(func) {
                filters.push(reg);
            }
        }
    }
    filters
}

/// Extract FilterReg from a function with #[register_filter] or #[rasmcore_macros::register_filter].
fn extract_filter_reg(func: &syn::ItemFn) -> Option<FilterReg> {
    for attr in &func.attrs {
        let path = attr.path();
        let is_register = path.is_ident("register_filter")
            || path
                .segments
                .last()
                .map(|s| s.ident == "register_filter")
                .unwrap_or(false);
        if !is_register {
            continue;
        }

        // Parse attribute arguments: name = "...", category = "...", etc.
        let mut name = None;
        let mut category = None;
        let mut group = String::new();
        let mut variant = String::new();
        let mut reference = String::new();
        let mut overlap = "zero".to_string();

        if let syn::Meta::List(meta_list) = &attr.meta {
            let tokens = meta_list.tokens.to_string();
            // Parse key = "value" pairs from the token stream
            name = extract_kv_string(&tokens, "name");
            category = extract_kv_string(&tokens, "category");
            group = extract_kv_string(&tokens, "group").unwrap_or_default();
            variant = extract_kv_string(&tokens, "variant").unwrap_or_default();
            reference = extract_kv_string(&tokens, "reference").unwrap_or_default();
            overlap = extract_kv_string(&tokens, "overlap").unwrap_or_else(|| "zero".to_string());
        }

        let name = name?;
        let category = category?;
        let fn_name = func.sig.ident.to_string();

        // Extract params from function signature, skipping pixels/info
        let params = extract_fn_params(&func.sig);

        return Some(FilterReg {
            name,
            category,
            group,
            variant,
            reference,
            overlap,
            fn_name,
            params,
        });
    }
    None
}

/// Extract named parameters from a function signature, skipping pixels/info.
fn extract_fn_params(sig: &syn::Signature) -> Vec<(String, String)> {
    let mut params = Vec::new();
    for input in &sig.inputs {
        if let syn::FnArg::Typed(pat_type) = input {
            let name = quote::quote!(#pat_type).to_string();
            // Skip pixels, info, and self params
            if name.contains("pixels")
                || name.contains("info")
                || name.contains("ImageInfo")
                || name.contains("self")
            {
                continue;
            }
            // Extract clean name and type
            let param_name = match pat_type.pat.as_ref() {
                syn::Pat::Ident(ident) => ident.ident.to_string(),
                _ => continue,
            };
            let param_type = type_to_string(&pat_type.ty);
            params.push((param_name, param_type));
        }
    }
    params
}

/// Convert a syn::Type to a clean string representation.
fn type_to_string(ty: &syn::Type) -> String {
    match ty {
        syn::Type::Reference(r) => {
            let inner = type_to_string(&r.elem);
            format!("&{inner}")
        }
        syn::Type::Slice(s) => {
            let inner = type_to_string(&s.elem);
            format!("[{inner}]")
        }
        syn::Type::Path(p) => p
            .path
            .segments
            .last()
            .map(|s| s.ident.to_string())
            .unwrap_or_default(),
        _ => quote::quote!(#ty).to_string().replace(' ', ""),
    }
}

/// Extract a key="value" pair from a stringified token stream.
fn extract_kv_string(tokens: &str, key: &str) -> Option<String> {
    let pattern = format!("{key} = \"");
    if let Some(start) = tokens.find(&pattern) {
        let rest = &tokens[start + pattern.len()..];
        if let Some(end) = rest.find('"') {
            return Some(rest[..end].to_string());
        }
    }
    None
}

/// Simple registration entry for generators/compositors/mappers (no params needed for manifest).
struct SimpleReg {
    name: String,
    category: String,
    group: String,
    variant: String,
    reference: String,
}

/// Parse registrations of a specific kind (e.g., "register_generator") via syn AST.
fn parse_registered_by_kind(source: &str, kind: &str) -> Vec<SimpleReg> {
    let file = match syn::parse_file(source) {
        Ok(f) => f,
        Err(_) => return Vec::new(),
    };

    let mut regs = Vec::new();
    for item in &file.items {
        if let syn::Item::Fn(func) = item {
            for attr in &func.attrs {
                let is_kind = attr
                    .path()
                    .segments
                    .last()
                    .map(|s| s.ident == kind)
                    .unwrap_or(false);
                if !is_kind {
                    continue;
                }
                if let syn::Meta::List(meta_list) = &attr.meta {
                    let tokens = meta_list.tokens.to_string();
                    if let Some(name) = extract_kv_string(&tokens, "name") {
                        regs.push(SimpleReg {
                            name,
                            category: extract_kv_string(&tokens, "category").unwrap_or_default(),
                            group: extract_kv_string(&tokens, "group").unwrap_or_default(),
                            variant: extract_kv_string(&tokens, "variant").unwrap_or_default(),
                            reference: extract_kv_string(&tokens, "reference").unwrap_or_default(),
                        });
                    }
                }
            }
        }
    }
    regs
}

/// Split `#[param(...)]` inner text by commas, respecting quoted strings and brackets.
/// e.g., `min = 0, max = 1, default = "[[0,0],[1,1]]"` → 3 parts, not 6.
fn split_param_attrs(s: &str) -> Vec<&str> {
    let mut parts = Vec::new();
    let mut start = 0;
    let mut in_quotes = false;
    let mut bracket_depth = 0i32;
    for (i, ch) in s.char_indices() {
        match ch {
            '"' => in_quotes = !in_quotes,
            '[' | '(' if !in_quotes => bracket_depth += 1,
            ']' | ')' if !in_quotes => bracket_depth -= 1,
            ',' if !in_quotes && bracket_depth == 0 => {
                parts.push(&s[start..i]);
                start = i + 1;
            }
            _ => {}
        }
    }
    if start < s.len() {
        parts.push(&s[start..]);
    }
    parts
}

/// Parse a string into a serde_json::Value. Handles "null", numbers, booleans,
/// JSON arrays/objects, and falls back to a JSON string for anything else.
fn parse_json_value(s: &str) -> Value {
    if s == "null" || s.is_empty() {
        return Value::Null;
    }
    // Try parsing as valid JSON (number, bool, array, object)
    if let Ok(v) = serde_json::from_str::<Value>(s) {
        return v;
    }
    // Fall back to a JSON string
    Value::String(s.to_string())
}

