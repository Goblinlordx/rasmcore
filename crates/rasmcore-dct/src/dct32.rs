/// Forward 32x32 DCT: spatial → frequency domain.
#[allow(dead_code)]
pub fn forward_dct_32x32(input: &[i16; 1024], output: &mut [i16; 1024]) {
    let mut tmp = [0i32; 1024];

    // Row pass
    for row in 0..32 {
        let s = row * 32;
        for freq in 0..32 {
            let mut sum = 0i64;
            for col in 0..32 {
                sum += MAT_DCT[freq][col] as i64 * input[s + col] as i64;
            }
            tmp[s + freq] = ((sum + 8) >> 4) as i32;
        }
    }

    // Column pass
    for freq in 0..32 {
        for col in 0..32 {
            let mut sum = 0i64;
            for row in 0..32 {
                sum += MAT_DCT[freq][row] as i64 * tmp[row * 32 + col] as i64;
            }
            output[freq * 32 + col] = ((sum + 1024) >> 11) as i16;
        }
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_idct_32x32_gradient_coefficients() {
        // Dequantized coefficients from gradient CTU(0,0) at QP=19
        let mut input = [0i16; 1024];
        input[0] = -12532; // DC at (0,0)
        input[1] = -2340; // freq 1 at (1,0)
        input[3] = -247; // freq 3 at (3,0)
        input[5] = -90; // freq 5
        input[7] = -45; // freq 7
        input[9] = -22; // freq 9
        input[11] = -22; // freq 11

        let mut output = [0i16; 1024];
        inverse_dct_32x32(&input, &mut output);

        // Expected spatial residual row 0:
        let expected_row0 = [
            -128, -127, -125, -123, -121, -119, -117, -115, -113, -111, -109, -107, -105, -103,
            -101, -99, -97, -95, -93, -91, -89, -87, -85, -83, -81, -79, -77, -75, -73, -71, -69,
            -68,
        ];

        for (i, (&got, &exp)) in output[..32].iter().zip(expected_row0.iter()).enumerate() {
            assert!(
                (got - exp).abs() <= 1,
                "Row 0 col {}: got {}, expected {} (diff {})",
                i,
                got,
                exp,
                got - exp
            );
        }
    }
}
