// Operations to skip (internal pipeline methods, not useful in chain builder)
const SKIP = new Set(['constructor', 'read', 'nodeInfo', 'composite', 'iccToSrgb', 'autoOrient']);

// Discover operations: only include methods that exist on Pipeline AND have param metadata
const operations = [];
const tempPipe = new Pipeline();
const proto = Object.getPrototypeOf(tempPipe);
const methodNames = Object.getOwnPropertyNames(proto)
  .filter(n => typeof proto[n] === 'function' && !SKIP.has(n) && !n.startsWith('write'));

for (const name of methodNames) {
  if (!PARAM_META[name]) continue;
  const rawCat = MANIFEST_CATEGORIES[name] || 'other';
  const category = CATEGORY_LABELS[rawCat] || rawCat.charAt(0).toUpperCase() + rawCat.slice(1);
  operations.push({ name, category, params: PARAM_META[name] });
}

