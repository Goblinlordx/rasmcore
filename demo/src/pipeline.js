// ─── Web Worker ─────────────────────────────────────────────────────────────

const worker = new Worker(new URL('./pipeline-worker.js', import.meta.url), { type: 'module' });
let workerReady = false;
let isProcessing = false;
let queuedRequest = null; // Single-slot queue: latest pending request
let imageInfo = null;     // { width, height } from loaded image

worker.postMessage({ type: 'init' });

function setProcessing(active) {
  isProcessing = active;
  const canvas = document.getElementById('preview-canvas');
  canvas.style.opacity = active ? '0.5' : '1';
  document.getElementById('total-time').textContent = active
    ? (queuedRequest ? 'Processing... (queued)' : 'Processing...')
    : document.getElementById('total-time').textContent;
}

function requestWorker(msg) {
  if (!workerReady) return;
  if (isProcessing) {
    queuedRequest = msg; // Replace any pending — single-slot
    setProcessing(true); // Update "queued" indicator
    return;
  }
  isProcessing = true;
  queuedRequest = null;
  setProcessing(true);
  worker.postMessage(msg, msg.imageBytes ? [msg.imageBytes] : []);
}

function serializeChain() {
  const chain = getChain();
  return chain.map(n => ({
    name: n.op.name,
    params: n.op.params,
    paramValues: { ...n.paramValues },
  }));
}

function serializeLayers() {
  return layers.filter(l => l.visible).map((l, idx) => ({
    id: l.id,
    chain: l.chain.map(n => ({
      name: n.op.name,
      params: n.op.params,
      paramValues: { ...n.paramValues },
    })),
    blendMode: idx === 0 ? null : (l.blendMode === 'over' ? null : l.blendMode),
    x: l.x,
    y: l.y,
  }));
}

function requestCompositeProcess() {
  if (layers.length === 0) return;
  if (layers.length === 1) {
    // Single layer — use simple process
    const copy = layers[0].imageBytes.buffer.slice(0);
    requestWorker({ type: 'load', imageBytes: copy }, [copy]);
    return;
  }
  // Multi-layer — send all layer data to worker
  const layerData = layers.filter(l => l.visible).map(l => ({
    id: l.id,
    imageBytes: l.imageBytes.buffer.slice(0),
    chain: l.chain.map(n => ({
      name: n.op.name,
      params: n.op.params,
      paramValues: { ...n.paramValues },
    })),
    blendMode: l === layers[0] ? null : (l.blendMode === 'over' ? null : l.blendMode),
    x: l.x,
    y: l.y,
  }));
  const transfers = layerData.map(l => l.imageBytes);
  requestWorker({ type: 'composite', layers: layerData }, transfers);
}

worker.onmessage = (e) => {
  const { type } = e.data;

  if (type === 'ready') {
    workerReady = true;
    return;
  }

  if (type === 'loaded') {
    imageInfo = e.data.info;
    compareBtn.style.display = 'inline-block';
    dropHint.style.display = 'block';
    if (imageInfo) {
      previewInfo.textContent = `${imageInfo.width}×${imageInfo.height}`;
    }
    // Trigger initial render
    requestWorker({ type: 'process', chain: serializeChain(), mode: 'full' });
    return;
  }

  if (type === 'result') {
    isProcessing = false;
    const blob = new Blob([e.data.png], { type: 'image/png' });
    const url = URL.createObjectURL(blob);
    const img = new Image();
    img.onload = () => {
      const canvas = e.data.mode === 'thumb'
        ? document.getElementById('thumb-preview')
        : previewCanvas;
      if (canvas) {
        canvas.width = img.width;
        canvas.height = img.height;
        canvas.getContext('2d').drawImage(img, 0, 0);
      }
      URL.revokeObjectURL(url);
    };
    img.src = url;

    // Update timings
    if (e.data.mode === 'full' && e.data.timings) {
      totalTimeEl.textContent = `Total: ${e.data.totalMs}ms (${e.data.timings.length} ops)`;
      totalTimeEl.style.color = '#4ade80';
      for (const t of e.data.timings) {
        for (const cn of chain) {
          if (cn.op.name === t.name) {
            cn.timingMs = t.ms;
            break;
          }
        }
      }
      // Update timing in cards
      for (const cn of chain) {
        const card = chainEl.querySelector(`[data-id="${cn.id}"]`);
        if (card) {
          const tEl = card.querySelector('.node-timing');
          if (tEl) tEl.textContent = `${cn.timingMs}ms`;
        }
      }
    }

    setProcessing(false);

    // Show Before/After button once we have a result to compare
    if (e.data.mode === 'full' && compareBtn) {
      compareBtn.style.display = '';
    }

    // Drain single-slot queue
    if (queuedRequest) {
      const next = queuedRequest;
      queuedRequest = null;
      requestWorker(next);
    }
    return;
  }

  if (type === 'exported') {
    isProcessing = false;
    setProcessing(false);
    const blob = new Blob([e.data.data], { type: e.data.mime });
    const a = document.createElement('a');
    a.href = URL.createObjectURL(blob);
    a.download = `rasmcore-pipeline.${e.data.format}`;
    a.click();
    return;
  }

  if (type === 'error') {
    isProcessing = false;
    setProcessing(false);
    totalTimeEl.textContent = `Error: ${e.data.message}`;
    totalTimeEl.style.color = '#f87171';
    // Drain queue even on error
    if (queuedRequest) {
      const next = queuedRequest;
      queuedRequest = null;
      requestWorker(next);
    }
    return;
  }
};

