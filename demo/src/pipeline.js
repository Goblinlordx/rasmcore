async function addLayer(file) {
  const bytes = new Uint8Array(await file.arrayBuffer());
  const layer = {
    id: nextLayerId++,
    name: file.name.replace(/\.[^.]+$/, ''),
    imageBytes: bytes,
    chain: [],
    blendMode: layers.length === 0 ? 'over' : 'over', // base layer has no blend
    x: 0,
    y: 0,
    visible: true,
    thumbUrl: null,
  };

  // Create thumbnail for layer card
  try {
    const blob = new Blob([bytes], { type: file.type || 'image/png' });
    layer.thumbUrl = URL.createObjectURL(blob);
  } catch (_) {}

  layers.push(layer);
  if (layers.length === 1) {
    activeLayerId = layer.id;
    // Render original for before/after
    try {
      const blob = new Blob([bytes], { type: file.type || 'image/png' });
      const url = URL.createObjectURL(blob);
      const img = new Image();
      img.onload = () => {
        originalCanvas.width = img.width;
        originalCanvas.height = img.height;
        originalCanvas.getContext('2d').drawImage(img, 0, 0);
        URL.revokeObjectURL(url);
      };
      img.src = url;
    } catch (_) {}
    compareBtn.style.display = 'inline-block';
  }

  renderLayers();
  renderChain();
  requestCompositeProcess();
}

function removeLayer(id) {
  layers = layers.filter(l => l.id !== id);
  if (activeLayerId === id) activeLayerId = layers.length > 0 ? layers[0].id : null;
  renderLayers();
  renderChain();
  requestCompositeProcess();
}

function setActiveLayer(id) {
  activeLayerId = id;
  editingNodeId = null;
  renderLayers();
  renderChain();
}

// ─── Layer Panel Rendering ─────────────────────────────────────────────────

const BLEND_MODES = ['over', 'multiply', 'screen', 'overlay', 'darken', 'lighten', 'soft-light', 'hard-light', 'difference', 'exclusion'];

function renderLayers() {
  layersEl.innerHTML = '';
  activeLayerLabel.textContent = activeLayer() ? `(${activeLayer().name})` : '';

  layers.forEach((layer, idx) => {
    const card = document.createElement('div');
    card.className = 'layer-card' + (layer.id === activeLayerId ? ' active' : '');
    card.addEventListener('click', () => setActiveLayer(layer.id));

    const header = document.createElement('div');
    header.className = 'layer-header';

    if (layer.thumbUrl) {
      const thumb = document.createElement('img');
      thumb.className = 'layer-thumb';
      thumb.src = layer.thumbUrl;
      header.appendChild(thumb);
    }

    const nameEl = document.createElement('span');
    nameEl.className = 'layer-name';
    nameEl.textContent = `${idx === 0 ? 'BG' : `L${idx}`}: ${layer.name}`;
    header.appendChild(nameEl);

    const visBtn = document.createElement('span');
    visBtn.className = 'layer-vis' + (layer.visible ? '' : ' hidden');
    visBtn.textContent = layer.visible ? '👁' : '👁‍🗨';
    visBtn.addEventListener('click', (e) => {
      e.stopPropagation();
      layer.visible = !layer.visible;
      renderLayers();
      requestCompositeProcess();
    });
    header.appendChild(visBtn);

    if (layers.length > 1) {
      const removeBtn = document.createElement('span');
      removeBtn.className = 'node-remove';
      removeBtn.textContent = '✕';
      removeBtn.addEventListener('click', (e) => { e.stopPropagation(); removeLayer(layer.id); });
      header.appendChild(removeBtn);
    }

    card.appendChild(header);

    // Blend mode + offset controls (not for base layer)
    if (idx > 0) {
      const controls = document.createElement('div');
      controls.className = 'layer-controls';

      const blendSelect = document.createElement('select');
      for (const mode of BLEND_MODES) {
        const opt = document.createElement('option');
        opt.value = mode; opt.textContent = mode;
        if (mode === layer.blendMode) opt.selected = true;
        blendSelect.appendChild(opt);
      }
      blendSelect.addEventListener('change', (e) => {
        e.stopPropagation();
        layer.blendMode = blendSelect.value;
        requestCompositeProcess();
      });
      controls.appendChild(blendSelect);

      const xInput = document.createElement('input');
      xInput.type = 'number'; xInput.value = layer.x; xInput.placeholder = 'X';
      xInput.addEventListener('change', (e) => { e.stopPropagation(); layer.x = parseInt(xInput.value) || 0; requestCompositeProcess(); });
      xInput.addEventListener('click', (e) => e.stopPropagation());
      controls.appendChild(xInput);

      const yInput = document.createElement('input');
      yInput.type = 'number'; yInput.value = layer.y; yInput.placeholder = 'Y';
      yInput.addEventListener('change', (e) => { e.stopPropagation(); layer.y = parseInt(yInput.value) || 0; requestCompositeProcess(); });
      yInput.addEventListener('click', (e) => e.stopPropagation());
      controls.appendChild(yInput);

      card.appendChild(controls);
    }

    layersEl.appendChild(card);
  });
}

// ─── Node Management ────────────────────────────────────────────────────────

function addNode(op) {
  const layer = activeLayer();
  if (!layer) { fileInput.click(); return; }
  const node = {
    id: nextId++,
    op,
    paramValues: Object.fromEntries(op.params.map(p => [p.name, p.default])),
    applied: true,
    timingMs: 0,
  };
  layer.chain.push(node);
  editingNodeId = node.id;
  renderChain();
  previewEditing();
}

function removeNode(id) {
  const layer = activeLayer();
  if (layer) layer.chain = layer.chain.filter(n => n.id !== id);
  if (editingNodeId === id) editingNodeId = null;
  renderChain();
  requestCompositeProcess();
}

function moveNode(fromIdx, toIdx) {
  const chain = getChain();
  if (fromIdx === toIdx || !chain.length) return;
  const [node] = chain.splice(fromIdx, 1);
  chain.splice(toIdx, 0, node);
  renderChain();
  requestCompositeProcess();
}

// ─── Render Chain ───────────────────────────────────────────────────────────

function renderChain() {
  chainEl.innerHTML = '';
  const chain = getChain();

  chain.forEach((node, idx) => {
    const card = document.createElement('div');
    card.className = 'node-card' + (editingNodeId === node.id ? ' editing' : '');
    card.dataset.id = node.id;
    card.dataset.idx = idx;
    card.draggable = true;

    // Drag handlers
    card.addEventListener('dragstart', (e) => {
      dragSrcIdx = idx;
      card.style.opacity = '0.4';
      e.dataTransfer.effectAllowed = 'move';
    });
    card.addEventListener('dragend', () => { card.style.opacity = '1'; dragSrcIdx = null; });
    card.addEventListener('dragover', (e) => {
      e.preventDefault();
      e.dataTransfer.dropEffect = 'move';
      card.style.borderTop = '2px solid #3b82f6';
    });
    card.addEventListener('dragleave', () => { card.style.borderTop = ''; });
    card.addEventListener('drop', (e) => {
      e.preventDefault();
      card.style.borderTop = '';
      if (dragSrcIdx !== null && dragSrcIdx !== idx) {
        moveNode(dragSrcIdx, idx);
      }
    });

    // Header
    const header = document.createElement('div');
    header.className = 'node-header';

    const nameSpan = document.createElement('span');
    nameSpan.className = 'node-name';
    nameSpan.textContent = `${idx + 1}. ${node.op.name}`;

    const actions = document.createElement('span');
    actions.style.cssText = 'display:flex;gap:6px;align-items:center';

    const timingSpan = document.createElement('span');
    timingSpan.className = 'node-timing';
    timingSpan.textContent = node.timingMs > 0 ? `${node.timingMs}ms` : '';

    if (editingNodeId !== node.id && node.op.params.length > 0) {
      const editBtn = document.createElement('span');
      editBtn.textContent = '✎';
      editBtn.style.cssText = 'cursor:pointer;color:#60a5fa;font-size:0.85rem';
      editBtn.title = 'Edit parameters';
      editBtn.addEventListener('click', () => { editingNodeId = node.id; renderChain(); previewEditing(); });
      actions.appendChild(editBtn);
    }

    const removeBtn = document.createElement('span');
    removeBtn.className = 'node-remove';
    removeBtn.textContent = '✕';
    removeBtn.addEventListener('click', () => removeNode(node.id));

    actions.appendChild(timingSpan);
    actions.appendChild(removeBtn);
    header.appendChild(nameSpan);
    header.appendChild(actions);
    card.appendChild(header);

    // Param summary (when not editing)
    if (editingNodeId !== node.id && node.op.params.length > 0) {
      const summary = document.createElement('div');
      summary.style.cssText = 'font-size:0.65rem;color:#666;margin-top:2px';
      summary.textContent = node.op.params.map(p => `${p.name}=${node.paramValues[p.name]}`).join(', ');
      card.appendChild(summary);
    }

    // Edit mode: param controls + preview + apply
    if (editingNodeId === node.id) {
      for (const p of node.op.params) {
        const paramDiv = document.createElement('div');
        paramDiv.className = 'node-param';

        if (p.type === 'color') {
          const label = document.createElement('label');
          label.textContent = p.label || p.name;
          paramDiv.appendChild(label);
          const input = document.createElement('input');
          input.type = 'color';
          input.value = node.paramValues[p.name] || '#808080';
          input.style.cssText = 'width:100%;height:28px;border:none;cursor:pointer';
          input.addEventListener('input', () => {
            node.paramValues[p.name] = input.value;
            schedulePreview();
          });
          paramDiv.appendChild(input);
        } else if (p.type === 'number') {
          const label = document.createElement('label');
          const valSpan = document.createElement('span');
          valSpan.textContent = node.paramValues[p.name];
          label.textContent = (p.label || p.name) + ' ';
          label.appendChild(valSpan);
          paramDiv.appendChild(label);

          const input = document.createElement('input');
          input.type = 'range';
          input.min = p.min; input.max = p.max; input.step = p.step;
          input.value = node.paramValues[p.name];
          input.addEventListener('input', () => {
            node.paramValues[p.name] = parseFloat(input.value);
            valSpan.textContent = input.value;
            schedulePreview(); // debounced small preview
          });
          paramDiv.appendChild(input);
        } else if (p.type === 'enum') {
          const label = document.createElement('label');
          label.textContent = p.label || p.name;
          paramDiv.appendChild(label);
          const select = document.createElement('select');
          for (const opt of p.options) {
            const option = document.createElement('option');
            option.value = opt; option.textContent = opt;
            if (opt === node.paramValues[p.name]) option.selected = true;
            select.appendChild(option);
          }
          select.addEventListener('change', () => {
            node.paramValues[p.name] = select.value;
            schedulePreview();
          });
          paramDiv.appendChild(select);
        }
        card.appendChild(paramDiv);
      }

      // Small preview canvas
      const previewWrap = document.createElement('div');
      previewWrap.style.cssText = 'margin-top:6px;text-align:center';
      const thumbCanvas = document.createElement('canvas');
      thumbCanvas.id = 'thumb-preview';
      thumbCanvas.style.cssText = 'max-width:100%;border:1px solid #333;border-radius:3px';
      previewWrap.appendChild(thumbCanvas);
      card.appendChild(previewWrap);

      // Apply button
      const btnRow = document.createElement('div');
      btnRow.style.cssText = 'margin-top:6px;display:flex;gap:6px';
      const applyBtn = document.createElement('button');
      applyBtn.textContent = 'Apply';
      applyBtn.addEventListener('click', () => {
        editingNodeId = null;
        renderChain();
        applyFullChain();
      });
      const cancelBtn = document.createElement('button');
      cancelBtn.textContent = 'Cancel';
      cancelBtn.className = 'secondary';
      cancelBtn.style.cssText = 'background:#333';
      cancelBtn.addEventListener('click', () => {
        editingNodeId = null;
        renderChain();
      });
      btnRow.appendChild(applyBtn);
      btnRow.appendChild(cancelBtn);
      card.appendChild(btnRow);
    }

    chainEl.appendChild(card);
  });
}

// ─── Preview (small thumbnail, debounced) ───────────────────────────────────

let previewTimer = null;
const PREVIEW_DEBOUNCE_MS = 1000; // Wait 1 second after last slider movement

function schedulePreview() {
  clearTimeout(previewTimer);
  previewTimer = setTimeout(previewEditing, PREVIEW_DEBOUNCE_MS);
}

function previewEditing() {
  const layer = activeLayer();
  if (!layer?.imageBytes || editingNodeId === null) return;
  requestWorker({ type: 'process', chain: serializeChain(), mode: 'thumb' });
}

// ─── Full Processing (on Apply) ─────────────────────────────────────────────

function applyFullChain() {
  const layer = activeLayer();
  if (!layer?.imageBytes) return;
  requestWorker({ type: 'process', chain: serializeChain(), mode: 'full' });
}

