#!/usr/bin/env node
/**
 * Generate the fluent RcImage SDK from jco-generated ImagePipeline types.
 *
 * Reads: demo/sdk/interfaces/rasmcore-image-pipeline.d.ts
 * Writes: sdk/typescript/src/rcimage.ts
 *
 * Usage: node scripts/generate-fluent-sdk.cjs [--naming camel|snake]
 *
 * --naming camel  (default) — JS/TS style: hueRotate, toJpeg
 * --naming snake  — Python style: hue_rotate, to_jpeg
 */

const fs = require('fs');
const path = require('path');

// Parse --naming flag
const namingArg = process.argv.find(a => a.startsWith('--naming'));
const namingIdx = process.argv.indexOf('--naming');
const NAMING = (namingIdx >= 0 && process.argv[namingIdx + 1]) || 'camel';
if (NAMING !== 'camel' && NAMING !== 'snake') {
  console.error(`ERROR: --naming must be "camel" or "snake", got "${NAMING}"`);
  process.exit(1);
}

// Naming conversion helpers
function camelToSnake(s) { return s.replace(/([A-Z])/g, '_$1').toLowerCase().replace(/^_/, ''); }
function convertName(name) { return NAMING === 'snake' ? camelToSnake(name) : name; }

const PROJECT_ROOT = path.resolve(__dirname, '..');
const PIPELINE_DTS = path.join(PROJECT_ROOT, 'demo/sdk/interfaces/rasmcore-image-pipeline.d.ts');
const OUTPUT_DIR = path.join(PROJECT_ROOT, 'sdk/typescript/src');
const OUTPUT_FILE = path.join(OUTPUT_DIR, 'rcimage.ts');

// Methods to skip (handled specially or not part of fluent API)
const SKIP_METHODS = new Set(['constructor', 'read', 'nodeInfo']);
// composite takes two NodeIds — handle specially
const SPECIAL_METHODS = new Set(['composite']);

function parseMethodSignature(line) {
  // Parse: methodName(param: Type, ...): ReturnType;
  const match = line.trim().match(/^(\w+)\(([^)]*)\):\s*(.+);$/);
  if (!match) return null;

  const [, name, paramsStr, returnType] = match;
  const params = paramsStr.split(',').map(p => p.trim()).filter(Boolean).map(p => {
    const [pName, ...pTypeParts] = p.split(':');
    return { name: pName.trim(), type: pTypeParts.join(':').trim() };
  });

  return { name, params, returnType: returnType.trim() };
}

function main() {
  if (!fs.existsSync(PIPELINE_DTS)) {
    console.error(`ERROR: ${PIPELINE_DTS} not found. Run demo/build.sh first.`);
    process.exit(1);
  }

  const source = fs.readFileSync(PIPELINE_DTS, 'utf-8');

  // Extract methods from ImagePipeline class
  const classMatch = source.match(/export class ImagePipeline \{([\s\S]*?)\n\}/);
  if (!classMatch) {
    console.error('ERROR: ImagePipeline class not found in .d.ts');
    process.exit(1);
  }

  const methodLines = classMatch[1].split('\n').map(l => l.trim()).filter(l => l && !l.startsWith('//'));
  const methods = methodLines.map(parseMethodSignature).filter(Boolean);

  const chainable = [];  // NodeId → NodeId (filter/transform operations)
  const terminal = [];   // NodeId → Buffer (write/encode operations)

  for (const m of methods) {
    if (SKIP_METHODS.has(m.name)) continue;
    if (SPECIAL_METHODS.has(m.name)) continue;

    if (m.returnType === 'NodeId') {
      // Chainable: first param is source: NodeId, rest are user params
      const userParams = m.params.slice(1); // skip source
      chainable.push({ ...m, userParams });
    } else if (m.returnType === 'Buffer') {
      // Terminal: first param is source: NodeId, rest are config
      const userParams = m.params.slice(1);
      terminal.push({ ...m, userParams });
    }
  }

  // Extract type imports needed
  const typeImports = new Set();
  for (const m of [...chainable, ...terminal]) {
    for (const p of m.userParams) {
      // Collect non-primitive types
      const baseType = p.type.replace(' | undefined', '').trim();
      if (baseType && !['number', 'string', 'boolean', 'Float32Array', 'Buffer'].includes(baseType)) {
        typeImports.add(baseType);
      }
    }
  }

  // Generate the SDK
  const lines = [];

  lines.push('// Auto-generated by scripts/generate-fluent-sdk.js');
  lines.push('// Do not edit — regenerate with: node scripts/generate-fluent-sdk.js');
  lines.push('//');
  lines.push(`// Generated from ${chainable.length} chainable + ${terminal.length} terminal operations`);
  lines.push('');
  lines.push("import type { ImagePipeline, NodeId, ImageInfo, Buffer as WasmBuffer } from '../../demo/sdk/interfaces/rasmcore-image-pipeline.js';");

  if (typeImports.size > 0) {
    const imports = [...typeImports].sort().join(', ');
    lines.push(`import type { ${imports} } from '../../demo/sdk/interfaces/rasmcore-image-pipeline.js';`);
  }

  lines.push('');
  lines.push('/**');
  lines.push(' * Fluent image processing chain.');
  lines.push(' *');
  lines.push(' * ```typescript');
  lines.push(" * const result = await rcimage.load(bytes)");
  lines.push(" *   .blur(3.0)");
  lines.push(" *   .resize(800, 600, 'lanczos3')");
  lines.push(" *   .toJpeg({ quality: 85 });");
  lines.push(' * ```');
  lines.push(' *');
  lines.push(' * All operations are lazy — pixels are only computed when a terminal');
  lines.push(' * method (toJpeg, toPng, etc.) is called.');
  lines.push(' */');
  lines.push('export class RcImage {');
  lines.push('  private pipeline: ImagePipeline;');
  lines.push('  private nodeId: number;');
  lines.push('');
  lines.push('  /** @internal Use rcimage.load() instead */');
  lines.push('  constructor(pipeline: ImagePipeline, nodeId: number) {');
  lines.push('    this.pipeline = pipeline;');
  lines.push('    this.nodeId = nodeId;');
  lines.push('  }');
  lines.push('');
  lines.push('  // ─── Runtime Validation ───');
  lines.push('');
  lines.push('  private static assertNumber(name: string, value: unknown): asserts value is number {');
  lines.push("    if (typeof value !== 'number' || Number.isNaN(value)) {");
  lines.push('      throw new TypeError(`${name} must be a number, got ${typeof value}`);');
  lines.push('    }');
  lines.push('  }');
  lines.push('');
  lines.push('  private static assertString(name: string, value: unknown): asserts value is string {');
  lines.push("    if (typeof value !== 'string') {");
  lines.push('      throw new TypeError(`${name} must be a string, got ${typeof value}`);');
  lines.push('    }');
  lines.push('  }');
  lines.push('');
  lines.push('  private static assertUint8Array(name: string, value: unknown): asserts value is Uint8Array {');
  lines.push('    if (!(value instanceof Uint8Array)) {');
  lines.push('      throw new TypeError(`${name} must be a Uint8Array, got ${typeof value}`);');
  lines.push('    }');
  lines.push('  }');
  lines.push('');
  lines.push('  /** Get image info (dimensions, format) without computing pixels. */');
  lines.push('  get info(): ImageInfo {');
  lines.push('    return this.pipeline.nodeInfo(this.nodeId);');
  lines.push('  }');
  lines.push('');
  lines.push('  /** Create a branch — both chains share the pipeline but diverge from here. */');
  lines.push('  fork(): RcImage {');
  lines.push('    return new RcImage(this.pipeline, this.nodeId);');
  lines.push('  }');

  // Generate chainable methods
  lines.push('');
  lines.push('  // ─── Chainable Operations (lazy, return RcImage) ───');

  for (const m of chainable) {
    // Public SDK names use the configured naming convention
    const pubName = convertName(m.name);
    const pubParams = m.userParams.map(p => ({ ...p, pubName: convertName(p.name) }));
    const paramSig = pubParams.map(p => `${p.pubName}: ${p.type}`).join(', ');
    // Internal pipeline call always uses camelCase (jco-generated)
    const paramCall = pubParams.map(p => p.pubName).join(', ');
    const callArgs = paramCall ? `this.nodeId, ${paramCall}` : 'this.nodeId';

    lines.push('');
    lines.push(`  ${pubName}(${paramSig}): RcImage {`);
    for (const p of pubParams) {
      if (p.type === 'number') {
        lines.push(`    RcImage.assertNumber('${p.pubName}', ${p.pubName});`);
      } else if (p.type === 'string') {
        lines.push(`    RcImage.assertString('${p.pubName}', ${p.pubName});`);
      }
    }
    lines.push(`    this.nodeId = this.pipeline.${m.name}(${callArgs});`);
    lines.push('    return this;');
    lines.push('  }');
  }

  // Generate terminal methods
  lines.push('');
  lines.push('  // ─── Terminal Operations (execute pipeline, return data) ───');

  for (const m of terminal) {
    // Convert writeJpeg → toJpeg, then apply naming convention
    let methodName = m.name;
    if (methodName.startsWith('write')) {
      methodName = 'to' + methodName.slice(5);
    }
    const pubName = convertName(methodName);
    const pubParams = m.userParams.map(p => ({ ...p, pubName: convertName(p.name) }));

    const paramSig = pubParams.map(p => {
      if (p.type.includes('| undefined')) {
        return `${p.pubName}?: ${p.type.replace(' | undefined', '')}`;
      }
      return `${p.pubName}: ${p.type}`;
    }).join(', ');

    const paramCall = pubParams.map(p => p.pubName).join(', ');
    const callArgs = paramCall ? `this.nodeId, ${paramCall}` : 'this.nodeId';

    lines.push('');
    lines.push(`  ${pubName}(${paramSig}): Uint8Array {`);
    lines.push(`    return this.pipeline.${m.name}(${callArgs}) as unknown as Uint8Array;`);
    lines.push('  }');
  }

  lines.push('}');
  lines.push('');

  // Generate the rcimage namespace / factory
  lines.push('/**');
  lines.push(' * rasmcore image processing — fluent chainable API.');
  lines.push(' *');
  lines.push(' * ```typescript');
  lines.push(" * import { rcimage } from '@rasmcore/sdk';");
  lines.push(' * const jpeg = rcimage.load(pngBytes).blur(3).resize(800, 600).toJpeg({ quality: 85 });');
  lines.push(' * ```');
  lines.push(' */');
  lines.push('export const rcimage = {');
  lines.push('  /**');
  lines.push('   * Load image data and start a processing chain.');
  lines.push('   * The image is not decoded until a terminal operation is called.');
  lines.push('   */');
  lines.push('  load(data: Uint8Array, pipelineModule?: { ImagePipeline: new () => ImagePipeline }): RcImage {');
  lines.push('    if (!(data instanceof Uint8Array)) {');
  lines.push("      throw new TypeError('load() requires a Uint8Array of image data');");
  lines.push('    }');
  lines.push('    if (data.length === 0) {');
  lines.push("      throw new RangeError('load() requires non-empty image data');");
  lines.push('    }');
  lines.push('    if (!pipelineModule) {');
  lines.push("      throw new Error('pipelineModule is required — pass the imported pipeline module');");
  lines.push('    }');
  lines.push('    const pipe = new pipelineModule.ImagePipeline();');
  lines.push('    const nodeId = pipe.read(data as unknown as WasmBuffer);');
  lines.push('    return new RcImage(pipe, nodeId);');
  lines.push('  },');
  lines.push('};');
  lines.push('');

  // Write output
  fs.mkdirSync(OUTPUT_DIR, { recursive: true });
  fs.writeFileSync(OUTPUT_FILE, lines.join('\n'));

  console.log(`Generated: ${OUTPUT_FILE}`);
  console.log(`  ${chainable.length} chainable operations`);
  console.log(`  ${terminal.length} terminal operations`);
  console.log(`  Methods: ${[...chainable, ...terminal].map(m => m.name).join(', ')}`);
}

main();
