#!/usr/bin/env node
/**
 * rasmcore-typegen — Generate typed RcImage wrapper from a WASM module.
 *
 * Usage:
 *   npx rasmcore-typegen ./rasmcore_image.wasm > src/rasmcore-typed.ts
 *   npx rasmcore-typegen ./rasmcore_image.wasm --output src/rasmcore-typed.ts
 *
 * Loads the WASM module, calls getFilterManifest() and getManifestHash(),
 * and emits a TypeScript file with typed methods for all operations.
 *
 * The generated class extends RcImage (dynamic runtime) — typed methods
 * just delegate to apply()/encode() with proper signatures.
 */

import * as fs from 'fs';
import * as path from 'path';

// -- Manifest types (inline to avoid import issues in standalone CLI) --

interface ParamMeta {
  name: string;
  type: string;
  min: number | null;
  max: number | null;
  step: number | null;
  default: unknown;
  label: string;
  hint: string;
}

interface OperationMeta {
  name: string;
  category: string;
  group: string;
  variant: string;
  reference: string;
  params: ParamMeta[];
}

interface Manifest {
  filters: OperationMeta[];
  [key: string]: unknown;
}

// -- Type mapping --

function rustTypeToTs(rustType: string): string {
  switch (rustType) {
    case 'f32': case 'f64': case 'u8': case 'u16': case 'u32': case 'u64':
    case 'i32': case 'i64':
      return 'number';
    case 'bool':
      return 'boolean';
    case 'String': case 'string':
      return 'string';
    default:
      if (rustType.startsWith('list<') || rustType.startsWith('&['))
        return 'number[]';
      return 'unknown';
  }
}

function snakeToCamel(s: string): string {
  return s.replace(/[-_](\w)/g, (_, c) => c.toUpperCase());
}

// -- Code generation --

function generateTypedSdk(manifest: Manifest, manifestHash: string): string {
  const lines: string[] = [];

  lines.push(`// Auto-generated by rasmcore-typegen`);
  lines.push(`// Module manifest hash: ${manifestHash}`);
  lines.push(`// Do not edit — regenerate with: npx rasmcore-typegen <module.wasm>`);
  lines.push(`//`);
  lines.push(`// ${manifest.filters.length} operations`);
  lines.push(``);
  lines.push(`import { RcImage as RcImageDynamic } from '@rasmcore/sdk';`);
  lines.push(`import type { WasmModule } from '@rasmcore/sdk';`);
  lines.push(``);
  lines.push(`const MANIFEST_HASH = '${manifestHash}';`);
  lines.push(``);
  lines.push(`/**`);
  lines.push(` * Typed image processing chain.`);
  lines.push(` *`);
  lines.push(` * Generated from a specific WASM module — provides autocomplete,`);
  lines.push(` * type checking, and parameter documentation for all operations.`);
  lines.push(` * Extends RcImageDynamic — all typed methods delegate to apply()/encode().`);
  lines.push(` */`);
  lines.push(`export class RcImage extends RcImageDynamic {`);
  lines.push(``);
  lines.push(`  static override load(module: WasmModule, data: Uint8Array): RcImage {`);
  lines.push(`    const base = RcImageDynamic.load(module, data);`);
  lines.push(`    if (base.manifestHash && base.manifestHash !== MANIFEST_HASH) {`);
  lines.push(`      console.warn(`);
  lines.push(`        \`@rasmcore/sdk: typed SDK was generated for module \${MANIFEST_HASH.slice(0, 8)}, \` +`);
  lines.push(`        \`but loaded module is \${base.manifestHash.slice(0, 8)}. \` +`);
  lines.push(`        \`Some methods may be missing or have changed. Regenerate with: npx rasmcore-typegen <module.wasm>\``);
  lines.push(`      );`);
  lines.push(`    }`);
  lines.push(`    // Reuse the base instance but re-brand as RcImage`);
  lines.push(`    Object.setPrototypeOf(base, RcImage.prototype);`);
  lines.push(`    return base as RcImage;`);
  lines.push(`  }`);
  lines.push(``);

  // Generate filter methods
  lines.push(`  // ─── Filter Operations ───`);
  lines.push(``);

  for (const op of manifest.filters) {
    const methodName = snakeToCamel(op.name);

    // JSDoc
    const doc = op.reference || `${op.name} filter`;
    const paramDocs = op.params.map(p => {
      let desc = p.label || p.name;
      if (p.min !== null && p.max !== null) desc += ` (${p.min}–${p.max})`;
      if (p.default !== null && p.default !== undefined) desc += `, default: ${p.default}`;
      return `   * @param ${snakeToCamel(p.name)} — ${desc}`;
    });

    lines.push(`  /** ${doc} */`);
    if (paramDocs.length > 0) {
      lines.push(`  /**`);
      for (const d of paramDocs) lines.push(d);
      lines.push(`   */`);
    }

    // Method signature
    const tsParams = op.params.map(p => {
      const tsName = snakeToCamel(p.name);
      const tsType = rustTypeToTs(p.type);
      return `${tsName}: ${tsType}`;
    });

    const sig = tsParams.length > 0
      ? `  ${methodName}(${tsParams.join(', ')}): RcImage {`
      : `  ${methodName}(): RcImage {`;
    lines.push(sig);

    // Build params object
    if (op.params.length > 0) {
      const paramObj = op.params
        .map(p => `${p.name}: ${snakeToCamel(p.name)}`)
        .join(', ');
      lines.push(`    return this.apply('${op.name}', { ${paramObj} }) as RcImage;`);
    } else {
      lines.push(`    return this.apply('${op.name}') as RcImage;`);
    }

    lines.push(`  }`);
    lines.push(``);
  }

  // Generate encode methods from write formats if available
  // These are discovered from the manifest or known conventions
  const commonFormats = ['jpeg', 'png', 'webp', 'avif', 'tiff', 'bmp', 'ico', 'qoi', 'gif'];
  lines.push(`  // ─── Encode Operations ───`);
  lines.push(``);

  for (const fmt of commonFormats) {
    const methodName = `to${fmt.charAt(0).toUpperCase() + fmt.slice(1)}`;
    lines.push(`  /** Encode as ${fmt.toUpperCase()}. */`);
    lines.push(`  ${methodName}(config?: Record<string, unknown>): Uint8Array {`);
    lines.push(`    return this.encode('${fmt}', config ?? {});`);
    lines.push(`  }`);
    lines.push(``);
  }

  lines.push(`}`);
  lines.push(``);

  return lines.join('\n');
}

// -- CLI --

async function main() {
  const args = process.argv.slice(2);
  let wasmPath: string | undefined;
  let outputPath: string | undefined;

  for (let i = 0; i < args.length; i++) {
    if (args[i] === '--output' || args[i] === '-o') {
      outputPath = args[++i];
    } else if (args[i] === '--help' || args[i] === '-h') {
      console.error('Usage: rasmcore-typegen <module.wasm> [--output <file.ts>]');
      console.error('');
      console.error('Generates a typed TypeScript SDK from a rasmcore WASM module.');
      console.error('Outputs to stdout by default, or to a file with --output.');
      process.exit(0);
    } else if (!wasmPath) {
      wasmPath = args[i];
    }
  }

  if (!wasmPath) {
    console.error('Usage: rasmcore-typegen <module.wasm> [--output <file.ts>]');
    process.exit(1);
  }

  // Load the WASM module
  const wasmBytes = fs.readFileSync(wasmPath);

  // Try to load as a jco-transpiled module
  // For CLI usage, we need to either:
  // 1. Use wasmtime-js or similar to instantiate the component
  // 2. Or parse the param-manifest.json directly if available alongside the wasm

  // Pragmatic approach: look for param-manifest.json next to the WASM file
  const manifestPath = path.join(path.dirname(wasmPath), 'param-manifest.json');
  const hashPath = path.join(path.dirname(wasmPath), 'param-manifest.hash');

  let manifest: Manifest;
  let manifestHash: string;

  if (fs.existsSync(manifestPath)) {
    manifest = JSON.parse(fs.readFileSync(manifestPath, 'utf8'));
    manifestHash = fs.existsSync(hashPath)
      ? fs.readFileSync(hashPath, 'utf8').trim()
      : '';
    console.error(`Read manifest from ${manifestPath} (${manifest.filters.length} operations)`);
  } else {
    console.error(`ERROR: param-manifest.json not found at ${manifestPath}`);
    console.error('');
    console.error('The typegen needs the manifest to generate typed wrappers.');
    console.error('Place param-manifest.json next to the WASM file, or build');
    console.error('the module first: cargo component build -p rasmcore-image');
    process.exit(1);
  }

  const output = generateTypedSdk(manifest, manifestHash);

  if (outputPath) {
    fs.mkdirSync(path.dirname(outputPath), { recursive: true });
    fs.writeFileSync(outputPath, output);
    console.error(`Generated: ${outputPath} (${manifest.filters.length} typed methods)`);
  } else {
    process.stdout.write(output);
  }
}

main().catch(err => {
  console.error(err);
  process.exit(1);
});
